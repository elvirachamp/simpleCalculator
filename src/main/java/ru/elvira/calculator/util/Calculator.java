package ru.elvira.calculator.util;

public class Calculator {
    public static int addNum(int num1, int num2) {
        return num1 + num2;
    }

    public static int subtractNum(int num1, int num2) {
        return num1 - num2;
    }

    public static int productNum(int num1, int num2) {
        return num1 * num2;
    }
}


