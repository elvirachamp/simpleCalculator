package ru.elvira.calculator.util;

public class Calculator {
    public static add () {
        int a;
        int b;
        System.out.println(a+b);
    }
    public static subtract () {
        int c;
        int d;
        System.out.println(c-d);
    }
    public static product () {
        int e;
        int f;
        System.out.println(e*f);
    }
}


