package ru.elvira.calculator.util;

public class Calculator {
    public static int addNum (int a, int b) {
        return a+b;
    }
    public static int subtractNum (int c, int d) {
        return c-d;
    }
    public static int productNum (int e, int f) {
        return e*f;
    }
}


